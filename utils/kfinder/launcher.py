"""
launcher.py
===========

Минимальный загрузчик программы K-Finder.

Назначение
----------
Этот файл специально держится максимально маленьким и стабильным.
Он нужен для двух задач:

1. Запустить приложение из внешнего Python-пакета `kfinder_app`,
   лежащего рядом с launcher.py.

2. Позволить обновлять рабочие модули программы без пересборки launcher.exe:
   достаточно заменить Python-файлы в папке проекта.

Принцип работы
--------------
- Определяет базовую папку приложения:
  - рядом с launcher.py в режиме разработки;
  - рядом с launcher.exe в собранном варианте.
- Добавляет базовую папку в sys.path.
- Импортирует функцию run() из kfinder_app.app.
- Запускает приложение.

Почему так
----------
В старом варианте весь проект жил в одном большом файле с собственной
точкой входа main(). Здесь мы отделяем "загрузчик" от основной логики,
чтобы:
- уменьшить размер и ответственность launcher-файла;
- дать возможность менять модули приложения отдельно;
- подготовить проект к разбиению на несколько файлов.

Сборка
------
Предпочтительный вариант:
    pyinstaller --noconfirm --clean --onedir --windowed ^
        --icon=kfinder.ico --name=kfinder launcher.py

Иконка kfinder.ico должна лежать рядом с launcher.py.
"""

from __future__ import annotations

import sys
from pathlib import Path


def get_base_dir() -> Path:
    """
    Возвращает базовую директорию приложения.

    Правила:
    - если программа собрана в .exe через PyInstaller, базой считается
      директория, где лежит launcher.exe;
    - если программа запущена как обычный .py-файл, базой считается
      директория, где лежит launcher.py.
    """
    if getattr(sys, "frozen", False):
        return Path(sys.executable).resolve().parent
    return Path(__file__).resolve().parent


BASE_DIR = get_base_dir()

# Делаем базовую папку доступной для обычного импорта:
# from kfinder_app.app import run
if str(BASE_DIR) not in sys.path:
    sys.path.insert(0, str(BASE_DIR))


def run() -> None:
    """
    Импортирует и запускает основное приложение.

    Импорт выполняется только внутри функции, а не на уровне модуля,
    чтобы launcher оставался максимально лёгким и не тянул лишнюю логику
    до фактического старта программы.
    """
    from kfinder_app.app import run as app_run
    app_run()


if __name__ == "__main__":
    run()