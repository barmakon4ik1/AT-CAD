"""
paths.py
========

Централизованное управление путями приложения.

Зачем нужен
-----------
В старой версии пути (APP_DIR, JSON, лог) были разбросаны по коду.
Теперь всё собрано в одном месте.

Этот модуль определяет:
- базовую директорию программы
- папку data
- пути к:
  - config.json
  - логам
  - всем JSON-индексам

И используется всеми остальными модулями.

Важно
-----
Никакой логики приложения здесь быть не должно — только пути.
"""

from __future__ import annotations

import sys
from pathlib import Path


# ----------------------------------------------------------------------
# Базовая директория
# ----------------------------------------------------------------------

def get_base_dir() -> Path:
    """
    Возвращает базовую директорию приложения.

    Правила:
    - .exe (PyInstaller) → папка рядом с exe
    - .py → папка проекта
    """
    if getattr(sys, "frozen", False):
        return Path(sys.executable).resolve().parent
    return Path(__file__).resolve().parent.parent


BASE_DIR = get_base_dir()


# ----------------------------------------------------------------------
# Папка data
# ----------------------------------------------------------------------

DATA_DIR = BASE_DIR / "data"
DATA_DIR.mkdir(parents=True, exist_ok=True)


# ----------------------------------------------------------------------
# Конфигурация
# ----------------------------------------------------------------------

CONFIG_FILE = DATA_DIR / "config.json"


# ----------------------------------------------------------------------
# Логирование
# ----------------------------------------------------------------------

LOG_FILE = DATA_DIR / "kfinder.log"


# ----------------------------------------------------------------------
# K-Index
# ----------------------------------------------------------------------

K_INDEX_JSON = DATA_DIR / "k_index.json"


# ----------------------------------------------------------------------
# DXF индексы
# ----------------------------------------------------------------------

DXF_RANGES_JSON = DATA_DIR / "dxf_ranges.json"
DXF_EXCEL_INDEX_JSON = DATA_DIR / "dxf_excel_index.json"
DXF_FILES_INDEX_JSON = DATA_DIR / "dxf_index.json"


# ----------------------------------------------------------------------
# App.Nr. индекс
# ----------------------------------------------------------------------

APPNR_INDEX_JSON = DATA_DIR / "appnr_index.json"
